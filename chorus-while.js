//while loops
const chorus = "Let's dance!";
let repeat = 0;
while (repeat < 10) {
  console.log(chorus);
  repeat++;
}
console.log("Until the sun comes up!");

//for loops--------------
const chorus = "Let's dance!";
for (let repeat = 0; repeat < 10; repeat++) {
  console.log("Until the sun comes up!");
}
